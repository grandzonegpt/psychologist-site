/* ── Apply translations ── */
function applyLang(lang) {
  const dict = T[lang];
  if (!dict) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });

  // Update html lang attribute
  document.documentElement.lang = lang === 'pl' ? 'pl' : 'ru';

  // Persist
  localStorage.setItem('lang', lang);

  // Notify calendar to re-render with new locale
  if (typeof renderCalendar === 'function') renderCalendar();

  // Reinit Cal.com embed with new locale
  if (typeof reinitCal === 'function') reinitCal(lang);
}

function initLang() {
  const saved = localStorage.getItem('lang') || 'ru';
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === saved);
    btn.addEventListener('click', () => {
      document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
      btn.classList.add('active');
      applyLang(btn.dataset.lang);
    });
  });
  applyLang(saved);
}

